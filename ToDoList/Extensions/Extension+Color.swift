import UIKit


extension UIColor {
    static let background: UIColor = UIColor(named: "backgroundColor") ?? .white
    static let text: UIColor = UIColor(named: "textColor") ?? .black
    static let subviewsBackground: UIColor = UIColor(named: "subviewsColor") ?? .white
    static let textGray: UIColor = UIColor(named: "textGrayColor") ?? .gray
    static let segment: UIColor = UIColor(named: "segmentColor") ?? .gray
    static let redColor: UIColor = UIColor(named: "redColor") ?? .red
    static let segmentSelected: UIColor = UIColor(named: "segmentSelectedColor") ?? .gray
    static let saveColor: UIColor = UIColor(named: "saveColor") ?? .blue
    static let separatorColor: UIColor = UIColor(named: "separatorColor") ?? .gray
    static let switcherColor: UIColor = UIColor(named: "switcherColor") ?? .gray
    
}
